<script>
document.addEventListener('DOMContentLoaded', function() {
  const toggle = document.getElementById('mobileToggle');
  const menu = document.getElementById('mobileMenu');
  
  // Toggle mobile menu
  toggle.addEventListener('click', function() {
    this.classList.toggle('active');
    menu.classList.toggle('active');
  });
  
  // Handle dropdowns
  const dropdowns = document.querySelectorAll('.menu-dropdown > a');
  dropdowns.forEach(link => {
    link.addEventListener('click', function(e) {
      if (window.innerWidth <= 991) {
        e.preventDefault();
        const submenu = this.nextElementSibling;
        const arrow = this.querySelector('.dropdown-arrow');
        
        // Close other dropdowns
        document.querySelectorAll('.submenu').forEach(menu => {
          if (menu !== submenu) {
            menu.classList.remove('active');
            menu.previousElementSibling.querySelector('.dropdown-arrow').classList.remove('active');
          }
        });
        
        // Toggle current dropdown
        submenu.classList.toggle('active');
        arrow.classList.toggle('active');
      }
    });
  });
  
  // Close menu when clicking links
  const menuLinks = document.querySelectorAll('.mobile-menu a:not(.menu-dropdown > a)');
  menuLinks.forEach(link => {
    link.addEventListener('click', function() {
      menu.classList.remove('active');
      toggle.classList.remove('active');
    });
  });
});
</script>
